class WeatherModel {
  String cityName;
  String dayDate;
  String weatherIcon;
  double temp;
  String desc;
  double feelTemp;
  double highTemp;
  double lowTemp;
  int humidity;
  double wind;
  int pressure;
  int sunrise;
  int sunset;
  WeatherModel({required this.cityName,required this.dayDate,required this
      .weatherIcon,required this.temp,required this.desc,required this
      .feelTemp,required this.highTemp,required this.lowTemp,required this
      .humidity,required this.wind,required this.pressure,required this
      .sunrise,required this.sunset});
}